// ---------------------------------------------------------------- config (project specifics; NOT this CLI)
function loadConfig() {
  const defaults = {
    projectName: 'project', defaultBranch: 'main', codePaths: ['src'], hasDb: false, hasDeploy: false,
    ownerFilesNote: 'current truth → current-state.md · queue → tasks.md · active slice → current-focus.md · history → worklog.md',
  };
  try { if (existsSync(F.config)) return { ...defaults, ...JSON.parse(readFileSync(F.config, 'utf8')) }; }
  catch { /* fall back to defaults on a malformed config */ }
  return defaults;
}
function codePaths() { const c = loadConfig().codePaths; return Array.isArray(c) && c.length ? c : ['src']; }
function codePrefixes() { return codePaths().map((p) => (p.endsWith('/') ? p : `${p}/`)); }

// ---------------------------------------------------------------- git helpers
function git(args, { allowFail = true } = {}) {
  try {
    return execFileSync('git', args, { cwd: REPO_ROOT, encoding: 'utf8', maxBuffer: 64 * 1024 * 1024 }).trim();
  } catch (e) {
    if (allowFail) return '';
    throw e;
  }
}
const gitHead = () => git(['rev-parse', 'HEAD']);
const gitHeadShort = () => git(['rev-parse', '--short', 'HEAD']);
const gitTree = () => git(['rev-parse', 'HEAD^{tree}']);
const gitBranch = () => git(['rev-parse', '--abbrev-ref', 'HEAD']);
const short = (sha) => (sha ? String(sha).slice(0, 7) : '');

// Parse `git status --porcelain=v1 -z`. NUL-separated; rename/copy records carry a 2nd NUL token
// (the original path) which we consume and ignore. Returns [{ x, y, path }] (path forward-slashed).
function parsePorcelainZ(raw) {
  const out = [];
  const tokens = raw.split('\0');
  for (let i = 0; i < tokens.length; i++) {
    const t = tokens[i];
    if (t.length < 4) continue; // skip '' tail and malformed
    const x = t[0];
    const y = t[1];
    const path = t.slice(3).replace(/\\/g, '/');
    out.push({ x, y, path });
    if (x === 'R' || x === 'C') i++; // porcelain v1: original-path token follows an R/C INDEX status only
  }
  return out;
}

// Fingerprint the CODE change-set (staged + unstaged + untracked + deleted), scoped to the configured
// code paths, with per-file content hashes. Clean tree → bind to commit + tree.
function computeFingerprint() {
  const head = gitHead();
  const tree = gitTree();
  if (!head || !tree) return 'error:git-unavailable'; // a broken/empty git must NOT masquerade as a clean tree
  const paths = codePaths();
  const prefixes = codePrefixes();
  let raw;
  try {
    // -uall expands untracked DIRECTORIES into individual files (else git collapses a new folder into
    // one `?? dir/` record — the exact new-file-in-a-new-folder case the fingerprint must hash).
    raw = execFileSync('git', ['status', '--porcelain=v1', '-z', '-uall', '--', ...paths],
      { cwd: REPO_ROOT, encoding: 'utf8', maxBuffer: 64 * 1024 * 1024 });
  } catch { return 'error:git-status-failed'; }
  const entries = parsePorcelainZ(raw)
    .filter((e) => prefixes.some((p) => e.path.startsWith(p)))
    .sort((a, b) => (a.path < b.path ? -1 : a.path > b.path ? 1 : 0));
  if (entries.length === 0) return `clean:${head}:${tree}`; // clean / post-commit binding
  const h = createHash('sha256');
  for (const e of entries) {
    h.update(`${e.x}${e.y} ${e.path}\n`);
    const abs = join(REPO_ROOT, e.path);
    if (e.x === 'D' || e.y === 'D') { h.update('DELETED\n'); continue; }
    if (e.path.endsWith('/')) { h.update('DIR\n'); continue; } // -uall avoids this; never readFileSync a dir
    try {
      if (statSync(abs).isDirectory()) { h.update('DIR\n'); continue; }
      h.update(createHash('sha256').update(readFileSync(abs)).digest('hex') + '\n');
    } catch { h.update('UNREADABLE\n'); } // an odd/transient path can never crash the gate
  }
  return `dirty:${head}:${h.digest('hex')}`;
}

// ---------------------------------------------------------------- IO (atomic + schemaVersion + revision + shape)
let tmpSeq = 0;
const tmpPath = (file) => `${file}.tmp.${process.pid}.${++tmpSeq}`; // unique suffix → safe under concurrent writers
// Shape contract: required top-level fields per control file, so valid-JSON-but-missing-a-field fails
// closed at read time instead of silently rendering `undefined`.
const REQUIRED_FIELDS = {
  'tasks.json': ['milestone', 'nextPath', 'snapshot', 'tasks'],
  'gates.json': ['head', 'diffFingerprint', 'lint', 'test', 'build', 'typecheck'],
  'approvals.json': ['approvals'],
};
function readJson(file) {
  if (!existsSync(file)) throw new Error(`missing control file: ${rel(file)} (run: ai-control init)`);
  let data;
  try { data = JSON.parse(readFileSync(file, 'utf8')); }
  catch (e) { throw new Error(`invalid JSON in ${rel(file)}: ${e.message}`); }
  if (data.schemaVersion !== SCHEMA_VERSION) {
    throw new Error(`schemaVersion mismatch in ${rel(file)}: got ${data.schemaVersion}, want ${SCHEMA_VERSION}`);
  }
  const base = file.split(/[\\/]/).pop();
  for (const f of (REQUIRED_FIELDS[base] || [])) if (!(f in data)) throw new Error(`${rel(file)}: missing required field "${f}" (shape validation)`);
  if (base === 'tasks.json' && !Array.isArray(data.tasks)) throw new Error(`${rel(file)}: "tasks" must be an array`);
  if (base === 'approvals.json' && !Array.isArray(data.approvals)) throw new Error(`${rel(file)}: "approvals" must be an array`);
  return data;
}
// Optimistic concurrency: caller passes the revision it read; we re-check just before writing.
function writeJsonAtomic(file, data, expectedRevision) {
  if (existsSync(file) && expectedRevision != null) {
    const cur = JSON.parse(readFileSync(file, 'utf8'));
    if (cur.revision !== expectedRevision) {
      throw new Error(`revision conflict on ${rel(file)}: read ${expectedRevision}, on disk ${cur.revision} — re-read and retry`);
    }
  }
  data.schemaVersion = SCHEMA_VERSION;
  data.revision = (data.revision ?? 0) + 1;
  const tmp = tmpPath(file);
  writeFileSync(tmp, `${JSON.stringify(data, null, 2)}\n`, 'utf8');
  renameSync(tmp, file); // atomic on the same volume
}
function writeFileAtomic(file, content) {
  const tmp = tmpPath(file);
  writeFileSync(tmp, content, 'utf8');
  renameSync(tmp, file);
}
function appendEvent(type, task, payload) {
  const lines = existsSync(F.events)
    ? readFileSync(F.events, 'utf8').split('\n').filter((l) => l.trim())
    : [];
  let maxId = 0; // monotonic: max over ALL parseable ids (a corrupt last line can't reset the counter)
  for (const l of lines) { try { const id = JSON.parse(l).id; if (Number.isInteger(id) && id > maxId) maxId = id; } catch { /* skip corrupt line */ } }
  const ev = { id: maxId + 1, ts: new Date().toISOString(), task: task ?? null, type, head: gitHead(), payload: payload ?? {} };
  writeFileAtomic(F.events, `${lines.join('\n')}${lines.length ? '\n' : ''}${JSON.stringify(ev)}\n`); // atomic temp+rename
  return ev;
}
const rel = (p) => p.replace(REPO_ROOT, '').replace(/\\/g, '/').replace(/^\//, '');
const norm = (s) => s.replace(/\r\n/g, '\n');

// `--key value` / bare `--flag` parser; positional args land in `_`.
function parseFlags(args) {
  const out = { _: [] };
  for (let i = 0; i < args.length; i++) {
    const a = args[i];
    if (a.startsWith('--')) {
      const key = a.slice(2);
      const next = args[i + 1];
      if (next === undefined || next.startsWith('--')) out[key] = true;
      else { out[key] = next; i++; }
    } else out._.push(a);
  }
  return out;
}
const splitList = (v, sep = ',') => (typeof v === 'string' && v.length ? v.split(sep).map((s) => s.trim()).filter(Boolean) : []);
// Hash a repo-relative doc path. Null if missing/dir/unreadable. For requiredDocs.
function docHash(p) {
  const abs = join(REPO_ROOT, p);
  try { if (!existsSync(abs) || statSync(abs).isDirectory()) return null; return createHash('sha256').update(readFileSync(abs)).digest('hex'); }
  catch { return null; }
}
// Canonical repo-relative doc path so ".ai/x", "./.ai/x", ".ai\\x" all key/compare identically.
function canonDoc(p) { return relative(REPO_ROOT, resolve(REPO_ROOT, String(p).replace(/\\/g, '/'))).replace(/\\/g, '/'); }

// ---------------------------------------------------------------- renderers (PURE: control state only, no wall clock)
function bullets(arr, indent = '') { return (arr || []).map((x) => `${indent}- ${x}`).join('\n'); }

function renderCurrentState(t, gates) {
  const s = t.snapshot || {};
  const g = gates;
  const out = [
    GEN_HEADER,
    '',
    '# Current State',
    '',
    `## Snapshot (${s.asOf || ''})`.trimEnd(),
    `- Branch \`${s.branch || 'main'}\` · HEAD = \`${short(g.head)}\` (last gated commit) · run \`ai-control status\` for live tree + gate freshness.`,
    `- Gates @ this HEAD: lint ${g.lint} · **${g.test?.count} tests** ${g.test?.pass ? 'passing' : 'not-passing'} · build ${g.build} · typecheck ${g.typecheck}.`,
  ];
  if (s.tag) out.push(`- Tag ${s.tag}.`);
  out.push('', '## Active milestone', `- **${t.milestone}**`, bullets(s.milestoneDetail));
  const at = activeTask(t);
  if (at) out.push('', '## Active task', `- **${at.id}** ${at.title} — taskState \`${at.state}\` · delivery \`${at.delivery || 'uncommitted'}\` (mode ${at.mode || '?'}, model ${at.model || '?'})`);
  out.push('', '## Next step', `- **${t.nextPath}** Candidates:`, bullets(s.nextPathCandidates, '  '));
  out.push('', '## Top risks', bullets(s.risks));
  out.push('', '## Environment', bullets(s.env));
  if (s.pointers) out.push('', '## Pointers', `- ${s.pointers}`);
  out.push('');
  return `${out.join('\n')}`;
}

function activeTask(t) {
  return (t.tasks || []).find((x) => x.state === 'in_progress' || x.state === 'review') || null;
}

function renderCurrentFocus(t) {
  const s = t.snapshot || {};
  const at = activeTask(t);
  const out = [
    GEN_HEADER,
    '',
    '# Current Focus',
    '',
    '## Milestone',
    `- **${t.milestone}**`,
    bullets(s.milestoneDetail),
    '',
    '## Active slice',
  ];
  if (at) {
    out.push(`- **${at.id} — ${at.title}**`);
    out.push(`  - taskState \`${at.state}\` · delivery \`${at.delivery || 'uncommitted'}\` · mode ${at.mode || '?'} · model ${at.model || '?'}`);
    if (at.scope?.in?.length) out.push(`  - In scope: ${at.scope.in.join('; ')}`);
    if (at.scope?.out?.length) out.push(`  - Out of scope: ${at.scope.out.join('; ')}`);
    if (at.requiredDocs?.length) out.push(`  - Required docs (${(at.docsReviewed || []).length}/${at.requiredDocs.length} reviewed): ${at.requiredDocs.join(', ')}`);
    if (at.checkpoints?.length) out.push(`  - Checkpoints: ${at.checkpoints.length}`);
  } else {
    out.push(`- **NONE — awaiting the next direction.** ${t.nextPath} Candidates live in \`tasks.md\` / \`current-state.md\`.`);
  }
  out.push('', `_(${loadConfig().ownerFilesNote})_`, '');
  return out.join('\n');
}

function renderTasks(t) {
  const s = t.snapshot || {};
  const b = t.backlog || {};
  const active = (t.tasks || []).filter((x) => ['ready', 'in_progress', 'review'].includes(x.state));
  const out = [GEN_HEADER, '', '# Tasks', '', '## Active queue'];
  if (active.length) {
    for (const x of active) out.push(`- **${x.id}** [${x.state} · ${x.delivery || 'uncommitted'}] ${x.title}`);
  } else {
    out.push(`- **NONE in flight.** ${t.milestone}. The next slice is the user's pick from the candidates below.`);
  }
  out.push('', '## Next-path candidates (PM confirms one with the user)', bullets(s.nextPathCandidates));
  out.push('', '## Product backlog', bullets(b.product));
  out.push('', '## Technical backlog', bullets(b.technical));
  out.push('', '## Do Not Do Now', bullets(b.doNotDoNow));
  out.push('');
  return out.join('\n');
}

function renderAll() {
  const t = readJson(F.tasks);
  const gates = readJson(F.gates);
  return {
    [GEN.state]: renderCurrentState(t, gates),
    [GEN.focus]: renderCurrentFocus(t),
    [GEN.tasks]: renderTasks(t),
  };
}

// ---------------------------------------------------------------- worklog (freeze history, render events below)
function readEvents() {
  if (!existsSync(F.events)) return [];
  const evs = [];
  for (const l of readFileSync(F.events, 'utf8').split('\n')) { if (!l.trim()) continue; try { evs.push(JSON.parse(l)); } catch { /* skip corrupt */ } }
  return evs;
}
function summarizePayload(e) {
  const p = e.payload || {};
  switch (e.type) {
    case 'control.init': return p.note || 'control plane initialized';
    case 'task.create': return `created "${p.title || ''}"` + (p.mode ? ` · mode ${p.mode}` : '') + (p.model ? ` · model ${p.model}` : '');
    case 'task.ready': return 'ready for a coder';
    case 'task.start': return `started (headAtStart ${short(p.headAtStart)})`;
    case 'task.checkpoint': return `checkpoint${p.note ? `: ${p.note}` : ''}`;
    case 'task.finish': return `finished → review${p.commit ? ` (${short(p.commit)})` : ''}${p.summary ? `: ${p.summary}` : ''}`;
    case 'task.accept': return 'accepted (PM gate)';
    case 'task.reopen': return 'reopened (blocked → ready)';
    case 'task.block': return `blocked${p.reason ? `: ${p.reason}` : ''}`;
    case 'task.abandon': return `abandoned${p.reason ? `: ${p.reason}` : ''}`;
    case 'delivery': return `delivery → ${p.delivery}${p.commit ? ` (${short(p.commit)})` : ''}`;
    case 'gate.result': return `gate ${p.gate} = ${p.value}`;
    default: return JSON.stringify(p);
  }
}
// PURE function of events.jsonl (stored ts/head — no wall clock). Newest first.
function renderWorklogGenerated() {
  const evs = readEvents().sort((a, b) => (b.id || 0) - (a.id || 0));
  const out = [GEN_HEADER, '', '## Control event log (generated from .ai/control/events.jsonl — newest first)', ''];
  if (!evs.length) out.push('_(no control events yet)_');
  for (const e of evs) out.push(`- **${e.ts}** · \`${e.type}\` · task ${e.task ? `\`${e.task}\`` : '—'} · @${short(e.head)} — ${summarizePayload(e)}`);
  out.push('');
  // Defensive: never let event text re-introduce the freeze marker into generated output.
  return out.join('\n').split(WORKLOG_MARKER).join('<!-- (marker text neutralized) -->');
}
// Freeze everything above the marker (verbatim); regenerate only below. First run treats the whole existing
// file as frozen legacy and appends the marker + generated section.
function syncWorklog() {
  const existing = existsSync(WORKLOG) ? readFileSync(WORKLOG, 'utf8') : '';
  const idx = existing.indexOf(WORKLOG_MARKER);
  if (idx !== existing.lastIndexOf(WORKLOG_MARKER)) {
    throw new Error('worklog.md has MULTIPLE freeze markers — refusing to render (it would silently drop history between them). Remove the stray marker(s) manually, then sync.');
  }
  let legacy;
  if (idx >= 0) legacy = existing.slice(0, idx); // verbatim — never touched again
  else if (!existing.length) legacy = '# Worklog\n\n> History above the marker is frozen. New control events render below.\n\n';
  else legacy = existing.endsWith('\n\n') ? existing : existing.endsWith('\n') ? `${existing}\n` : `${existing}\n\n`;
  const content = `${legacy}${WORKLOG_MARKER}\n\n${renderWorklogGenerated()}`;
  const before = existsSync(WORKLOG) ? norm(readFileSync(WORKLOG, 'utf8')) : null;
  if (before !== norm(content)) { writeFileAtomic(WORKLOG, content); return 'updated'; }
  return 'unchanged';
}

// ---------------------------------------------------------------- commands
function ensureControlDir() { if (!existsSync(CONTROL_DIR)) mkdirSync(CONTROL_DIR, { recursive: true }); }

function cmdSync() {
  const rendered = renderAll();
  const written = [];
  for (const [file, content] of Object.entries(rendered)) {
    const before = existsSync(file) ? norm(readFileSync(file, 'utf8')) : null;
    if (before !== norm(content)) { writeFileAtomic(file, content); written.push(`${rel(file)} (updated)`); }
    else written.push(`${rel(file)} (unchanged)`);
  }
  const wl = syncWorklog();
  written.push(`${rel(WORKLOG)} — below freeze marker (${wl}); history above the marker is frozen`);
  console.log('sync — generated views:');
  written.forEach((w) => console.log(`  ${w}`));
  console.log('(content ABOVE the worklog marker + authored files are never touched.)');
  return 0;
}

function gateFreshness(gates) {
  const headNow = gitHead();
  const fpNow = computeFingerprint();
  const headOk = gates.head === headNow;
  const fpOk = gates.diffFingerprint === fpNow;
  return { headOk, fpOk, fresh: headOk && fpOk, headNow, fpNow };
}

function cmdStatus() {
  const t = readJson(F.tasks);
  const gates = readJson(F.gates);
  const approvals = readJson(F.approvals);
  const cfg = loadConfig();
  const fr = gateFreshness(gates);
  const counts = {};
  for (const x of t.tasks || []) counts[x.state] = (counts[x.state] || 0) + 1;
  const at = activeTask(t);
  const pendingApprovals = (approvals.approvals || []).filter((a) => !a.used);

  console.log('== ai-control status ==');
  console.log(`project   : ${cfg.projectName}`);
  console.log(`milestone : ${t.milestone}`);
  console.log(`next path : ${t.nextPath}`);
  const treeLabel = fr.fpNow.startsWith('clean:') ? 'CLEAN' : fr.fpNow.startsWith('error:') ? `UNAVAILABLE (${fr.fpNow})` : 'DIRTY';
  console.log(`git       : ${gitBranch() || '(no branch)'} @ ${gitHeadShort() || '(no HEAD)'} · tree ${treeLabel}`);
  console.log(`gates     : @${short(gates.head)} lint=${gates.lint} test=${gates.test?.count}${gates.test?.pass ? '' : '(not-pass)'} build=${gates.build} typecheck=${gates.typecheck} -> ${fr.fresh ? 'FRESH' : 'STALE (HEAD/diff moved since gates ran)'}`);
  if (!t.tasks?.length) {
    console.log('tasks     : none (create one: ai-control task create --id <ID> --title "...")');
  } else {
    console.log(`tasks     : ${t.tasks.length} total — ${Object.entries(counts).map(([k, v]) => `${k}:${v}`).join(' ')}`);
    if (at) {
      console.log(`  active  : ${at.id} — ${at.title}`);
      console.log(`            taskState=${at.state} · deliveryState=${at.delivery || 'uncommitted'} · approvals=${pendingApprovals.filter((a) => a.task === at.id).length} pending`);
    }
  }
  console.log(`approvals : ${pendingApprovals.length} pending, ${(approvals.approvals || []).length} total`);
  return 0;
}

function cmdCheck() {
  const findings = [];
  let t, gates, approvals;
  try { t = readJson(F.tasks); } catch (e) { findings.push(`tasks.json: ${e.message}`); }
  try { gates = readJson(F.gates); } catch (e) { findings.push(`gates.json: ${e.message}`); }
  try { approvals = readJson(F.approvals); } catch (e) { findings.push(`approvals.json: ${e.message}`); }
  if (!existsSync(F.events)) findings.push('events.jsonl: missing');
  else {
    const evLines = readFileSync(F.events, 'utf8').split('\n').filter((l) => l.trim());
    const ids = new Set();
    let bad = false; let dup = false;
    for (const l of evLines) { let o; try { o = JSON.parse(l); } catch { bad = true; continue; } if (ids.has(o.id)) dup = true; else ids.add(o.id); }
    if (bad) findings.push('events.jsonl: contains a non-JSON line');
    if (dup) findings.push('events.jsonl: duplicate event id(s) — monotonic-id invariant broken');
  }

  if (gates) {
    const fr = gateFreshness(gates);
    if (!fr.headOk) findings.push(`gates.json STALE: recorded head ${short(gates.head)} != current HEAD ${short(fr.headNow)}`);
    if (!fr.fpOk) findings.push(`gates.json STALE: diffFingerprint != current code fingerprint (code changed since gates ran)`);
  }

  if (t && gates) {
    let rendered;
    try { rendered = renderAll(); } catch (e) { findings.push(`render failed: ${e.message}`); rendered = null; }
    if (rendered) {
      for (const [file, content] of Object.entries(rendered)) {
        if (!existsSync(file)) findings.push(`${rel(file)}: not generated yet (run sync)`);
        else if (norm(readFileSync(file, 'utf8')) !== norm(content)) findings.push(`${rel(file)}: DRIFT — on-disk content != render of control state (was it hand-edited?)`);
      }
    }
  }

  for (const x of (t?.tasks || [])) {
    if (!x.id || typeof x.id !== 'string') findings.push(`a task is missing a string "id" (title: ${x.title || '?'})`);
    if (!TASK_STATES.includes(x.state)) findings.push(`task ${x.id}: invalid state "${x.state}"`);
    if (x.delivery && !DELIVERY_STATES.includes(x.delivery)) findings.push(`task ${x.id}: invalid delivery "${x.delivery}"`);
    if (x.delivery && x.delivery !== 'uncommitted' && !['in_progress', 'review', 'accepted'].includes(x.state)) findings.push(`task ${x.id}: deliveryState "${x.delivery}" on a "${x.state}" task (contradictory 3-axis state)`);
  }

  for (const a of (approvals?.approvals || [])) {
    if (a.task && !(t?.tasks || []).some((x) => x.id === a.task)) findings.push(`approval ${a.id}: references unknown task ${a.task}`);
  }

  // requiredDocs: for an ACTIVE task, verify the docsChanged DECLARATION by hash (machine checks the
  // declaration; the PM checks semantics).
  for (const x of (t?.tasks || [])) {
    if (x.state !== 'in_progress' && x.state !== 'review') continue;
    const base = x.docBaselines || {};
    for (const p of (x.requiredDocs || [])) {
      const cur = docHash(p);
      if (cur == null) { findings.push(`task ${x.id}: requiredDoc ${p} missing on disk`); continue; }
      const changed = (base[p] ?? null) !== cur; // null baseline (doc created during the task) counts as changed
      const declared = (x.docsChanged || []).includes(p);
      if (changed && !declared) findings.push(`task ${x.id}: requiredDoc ${p} CHANGED/created since start but not declared in docsChanged`);
      if (declared && !changed) findings.push(`task ${x.id}: ${p} declared in docsChanged but its hash is unchanged since start`);
    }
    if (x.state === 'review') {
      const notReviewed = (x.requiredDocs || []).filter((p) => !(x.docsReviewed || []).includes(p));
      if (notReviewed.length) findings.push(`task ${x.id}: requiredDoc(s) not declared reviewed before finish: ${notReviewed.join(', ')}`);
    }
  }

  // worklog: the section BELOW the marker must match the event render; history ABOVE is frozen (never checked).
  if (existsSync(WORKLOG)) {
    const w = readFileSync(WORKLOG, 'utf8');
    const wi = w.indexOf(WORKLOG_MARKER);
    if (wi < 0) findings.push('worklog.md: missing the ai-control freeze marker (run sync to freeze history + render events)');
    else if (wi !== w.lastIndexOf(WORKLOG_MARKER)) findings.push('worklog.md: MULTIPLE freeze markers — do NOT run sync (it would drop history between them); remove the stray marker(s) manually');
    else {
      const below = norm(w.slice(wi + WORKLOG_MARKER.length)).replace(/^\n+/, '').trimEnd();
      const expected = norm(renderWorklogGenerated()).trimEnd();
      if (below !== expected) findings.push('worklog.md: DRIFT below the freeze marker (hand-edited below the line? run sync)');
    }
  }

  if (findings.length === 0) { console.log('ai-control check: OK — control state consistent, gates fresh, generated views in sync.'); return 0; }
  console.error('ai-control check: FAIL');
  findings.forEach((f) => console.error(`  - ${f}`));
  return 1;
}

