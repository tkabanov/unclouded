// internal: exercise the atomic-write/revision path + bind gates to the current change-set.
function cmdGateSet(name, passStr) {
  const gates = readJson(F.gates);
  const rev = gates.revision;
  if (name === 'test') {
    const count = Number.parseInt(passStr, 10);
    if (!Number.isFinite(count) || count < 0) throw new Error('usage: gate set test <count>');
    gates.test = { pass: true, count };
  } else {
    const valid = ['lint', 'build', 'typecheck'];
    if (!valid.includes(name)) throw new Error(`unknown gate "${name}" (expected ${valid.join('|')}|test)`);
    gates[name] = passStr === 'pass' || passStr === 'true' ? 'pass' : 'fail';
  }
  gates.head = gitHead();
  gates.diffFingerprint = computeFingerprint();
  gates.recordedAt = new Date().toISOString();
  writeJsonAtomic(F.gates, gates, rev);
  appendEvent('gate.result', null, { gate: name, value: name === 'test' ? gates.test : gates[name] });
  const valueLabel = name === 'test' ? `${gates.test.count} pass` : gates[name];
  console.log(`gate ${name} = ${valueLabel} (rebound to HEAD ${gitHeadShort()}; gates.json revision ${rev} -> ${rev + 1})`);
  return 0;
}

