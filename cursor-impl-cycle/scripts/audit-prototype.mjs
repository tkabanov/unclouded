#!/usr/bin/env node
/**
 * Scan frontend/ prototype and emit cursor-impl-cycle/config/prototype-inventory.json
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { loadProjectConfig, resolvePaths } from "../lib/paths.mjs";
import { resolveWorkspaceRoot } from "../lib/workspace.mjs";

const PACK_ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");

function readRoutes(appTsxPath) {
  if (!fs.existsSync(appTsxPath)) return [];
  const content = fs.readFileSync(appTsxPath, "utf8");
  const routes = [];
  const routeRe = /<Route\s+path="([^"]+)"/g;
  let match;
  while ((match = routeRe.exec(content)) !== null) {
    routes.push({ path: match[1], source: "App.tsx" });
  }
  return routes;
}

function findPageFile(frontendSrc, candidates) {
  for (const c of candidates) {
    const full = path.join(frontendSrc, c);
    if (fs.existsSync(full)) return c;
  }
  const pagesDir = path.join(frontendSrc, "pages");
  if (!fs.existsSync(pagesDir)) return candidates[0];
  const names = fs.readdirSync(pagesDir);
  for (const c of candidates) {
    const base = path.basename(c);
    const match = names.find((n) => n.toLowerCase() === base.toLowerCase());
    if (match) return `pages/${match}`;
  }
  return candidates[0];
}

function globTsx(dir, base = dir) {
  const results = [];
  if (!fs.existsSync(dir)) return results;
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      if (entry.name === "ui") continue;
      results.push(...globTsx(full, base));
    } else if (/\.(tsx|ts)$/.test(entry.name)) {
      results.push(path.relative(base, full).replace(/\\/g, "/"));
    }
  }
  return results;
}

function areaForFile(relPath) {
  if (relPath.startsWith("pages/")) return "pages";
  if (relPath.startsWith("hooks/")) return "auth";
  if (relPath.startsWith("integrations/")) return "integrations";
  if (relPath.startsWith("lib/")) return "lib";
  if (relPath.includes("Onboarding")) return "onboarding";
  if (relPath.includes("Chat") || relPath.includes("ai-elements")) return "chat";
  if (relPath.includes("Dashboard") || relPath.includes("AppSidebar") || relPath.includes("DashboardLayout"))
    return "shell";
  if (relPath.includes("Crisis")) return "crisis";
  if (relPath.includes("Journal")) return "journal";
  if (relPath.includes("Path")) return "paths";
  if (relPath.includes("Settings") || relPath.includes("Subscription")) return "settings";
  if (relPath.includes("Auth") || relPath.includes("Protected")) return "auth";
  return "components";
}

function main() {
  const workspaceRoot = resolveWorkspaceRoot(PACK_ROOT);
  const project = loadProjectConfig(PACK_ROOT);
  const paths = resolvePaths(workspaceRoot, project, PACK_ROOT);
  const frontendSrc = path.join(paths.frontendAppDir, "src");
  const appTsx = path.join(frontendSrc, "App.tsx");

  const routeEntries = readRoutes(appTsx).map((r) => {
    const pageName = r.path === "/" ? "Index" : r.path.split("/").filter(Boolean)[0];
    const pageFile = `pages/${pageName.charAt(0).toUpperCase()}${pageName.slice(1)}.tsx`;
    const candidates = [
      `pages/${pageName}.tsx`,
      pageFile,
      `pages/${pageName.charAt(0).toUpperCase()}${pageName.slice(1).replace(/:.*$/, "")}.tsx`,
    ];
    const file = findPageFile(frontendSrc, candidates);
    return {
      path: r.path,
      file: `${project.frontend_app_dir ?? "frontend"}/src/${file}`,
      status: fs.existsSync(path.join(frontendSrc, file)) ? "prototype" : "planned",
    };
  });

  const relFiles = globTsx(frontendSrc);
  const componentsByArea = {};
  for (const rel of relFiles) {
    const area = areaForFile(rel);
    if (!componentsByArea[area]) componentsByArea[area] = [];
    componentsByArea[area].push(`${project.frontend_app_dir ?? "frontend"}/src/${rel}`);
  }

  const inventory = {
    generated_at: new Date().toISOString(),
    stack: "react-vite-tsx",
    frontend_app_dir: project.frontend_app_dir ?? "frontend",
    routes: routeEntries,
    files_by_area: componentsByArea,
    key_files: {
      routes: `${project.frontend_app_dir ?? "frontend"}/src/App.tsx`,
      auth_guard: `${project.frontend_app_dir ?? "frontend"}/src/components/ProtectedRoute.tsx`,
      supabase_client: `${project.frontend_app_dir ?? "frontend"}/src/integrations/supabase/client.ts`,
      auth_dialog: `${project.frontend_app_dir ?? "frontend"}/src/components/AuthDialog.tsx`,
      onboarding_page: `${project.frontend_app_dir ?? "frontend"}/src/pages/Onboarding.tsx`,
      dashboard_layout: `${project.frontend_app_dir ?? "frontend"}/src/components/DashboardLayout.tsx`,
    },
    notes:
      "Auto-generated by scripts/audit-prototype.mjs. Implementers must re-read actual files before editing.",
  };

  fs.mkdirSync(path.dirname(paths.prototypeInventoryPath), { recursive: true });
  fs.writeFileSync(paths.prototypeInventoryPath, `${JSON.stringify(inventory, null, 2)}\n`, "utf8");
  console.log(`Wrote ${paths.prototypeInventoryPath}`);
  console.log(`  routes: ${routeEntries.length}, files: ${relFiles.length}`);
}

main();
